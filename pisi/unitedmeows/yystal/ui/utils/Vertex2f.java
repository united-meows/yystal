package pisi.unitedmeows.yystal.ui.utils;

public class Vertex2f {
	public float x, y;
	public Vertex2f(float _x, float _y) {
		x = _x;
		y = _y;
	}

	public float x() {
		return x;
	}

	public float y() {
		return y;
	}

	public void setX(float x) {
		this.x = x;
	}

	public void setY(float y) {
		this.y = y;
	}
}
